package io.codearcs.candlestack.aws.s3;


public class S3Location {

	private String id, name, bucket, key;


	S3Location() {}


	public String getId() {
		return id;
	}


	public String getName() {
		return name;
	}


	public String getBucket() {
		return bucket;
	}


	public String getKey() {
		return key;
	}


}
